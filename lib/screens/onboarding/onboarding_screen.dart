      key: introKey,
      globalBackgroundColor: Colors.white,
      allowImplicitScrolling: true,
      autoScrollDuration: 3000,
      globalHeader: Align(
        alignment: Alignment.topRight,
        child: SafeArea(
          child: Padding(
            padding: const EdgeInsets.only(top: 16, right: 16),
            child: _buildImage('plmseal.png', 100),
          ),
        ),
      ),
      globalFooter: SizedBox(
        width: double.infinity,
        height: 60,
        child: ElevatedButton(
          child: const Text(
            'Let\'s go right away!',
            style: TextStyle(fontSize: 16.0, fontWeight: FontWeight.bold),
          ),
          onPressed: () => _onIntroEnd(context),
        ),
      ),
      pages: [
        PageViewModel(
          title: "Welcome PLMAyers!",
          body:
          "Hi, welcome to our new, easy and mobile friendly app of Pamantasan ng Lungsod ng Maynila.",
          image: _buildImage('On1.jpg', width),
          decoration: pageDecoration,
        ),
        PageViewModel(
          title: "Be Updated!",
          body:
          "Be informed of your classes, PLM updates, admissions, enrollment, PLMAT results, and more.",
          image: _buildImage('On2.jpg', width),
          decoration: pageDecoration,
